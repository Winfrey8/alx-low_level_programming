#include <stdio.h>

/**
 * main - print alphabets in lower case except q and e
 *
 * Description: using the main function
 * the program prints alphabets in lower case
 * Return: 0
 */
int main(void)
{
	char ch;

	for (ch = 'a' ; ch <= 'z' ; ch++)
	{
	if (ch = !'e' && ch = !'q')
	{
		putchar(ch);
	}
	}
putchar("\n");
return (0);
}

